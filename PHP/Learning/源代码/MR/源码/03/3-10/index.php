<?php
$m = 6 ;//运算时将6转换为二进制码110
$n = 12 ;//运算时将12转换为二进制码1100
$mn = $m & $n ;             //做与操作后转换为十进制
echo $mn ."<br>" ;
$mn = $m | $n ;              //做或操作后转换为十进制
echo $mn ."<br>" ;
$mn = $m ^ $n ;             //做异或操作后转换为十进制
echo $mn ."<br>" ;
$mn = ~$m ;                //做非操作后转换为十进制
echo $mn ."<br>" ;
?>